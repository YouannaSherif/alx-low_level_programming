#include "3-calc.h"

/**
 * op_add -adds nums
 *
 * @a: a parameter
 * @b: a parameter
 *
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub -substract nums
 *
 * @a: a parameter
 * @b: a parameter
 *
 * Return: sub of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul -multiplies numbers
 *
 * @a: a parameter
 * @b: a parameter
 *
 * Return: mul of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div -divides numbers
 *
 * @a: a parameter
 * @b: a parameter
 *
 * Return: div of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod -remainder of division
 *
 * @a: a parameter
 * @b: a parameter
 *
 * Return: remainder of division
 */

int op_mod(int a, int b)
{
	return (a % b);
}
